package org.HotelRecommand.model;

public class HotelModel {

	private int hid;
	   private String hname;
	   private String hadd;
	   private String hcon;
	   private String htype;
	   private int areaid;
	
	public int getHid() {
		return hid;
	}
	public void setHid(int hid) {
		this.hid = hid;
	}
	public String getHname() {
		return hname;
	}
	public void setHname(String hname) {
		this.hname = hname;
	}
	public String getHadd() {
		return hadd;
	}
	public void setHadd(String hadd) {
		this.hadd = hadd;
	}
	public String getHcon() {
		return hcon;
	}
	public void setHcon(String hcon) {
		this.hcon = hcon;
	}
	public String getHtype() {
		return htype;
	}
	public void setHtype(String htype) {
		this.htype = htype;
	}
	public int getAreaid() {
		return areaid;
	}
	public void setAreaid(int areaid) {
		this.areaid = areaid;
	}
	
}
